    <h2 href="#">Introduction</h2>
    <p>
      The Flexibility Structure Analyzer is a web-based tool that allows people who wish to design a flexible system to check and verify whether their designed system's structure is as efficient or robust as a fully flexible system and to compare it with systems. Thus, the analyzer not only provides a quick way for users to check their designed flexible systems, but also facilitates the design process. <br><br>
    </p>

    <h2 href="#">Terminology</h2>
    <p>
      <b>Number of tasks:</b> the number of tasks that are coming into the system (default = 5).
      <br>
      <b>Number of servers:</b> the number of servers in the system (default = 5).
      <br>
      <b>Training:</b> whether a server is configured to accept a particular task.
      <br>
      <b>Flexibility Structure:</b> the training of each server.
      <br>
      <b>λ (arrival rate):</b> the rate at which each task is arriving in the system (default = 1).
      <br>
      <b>μ (potential server rates):</b> the rate at which each server can process each task (default = 0).
      <br>
      <b>Efficient:</b> the system can handle the same load as a fully flexible system.
      <br>
      <b>Robust:</b> the system is able to shift excess capacity from any task to any other task.
      <br>
      <b>Reference:</b> S. Andradottir, H. Ayhan, and D.G. Down, Design Principles for Flexible Systems Production and Operations Management, 2013 (volume 22, issue 5), 1144-1156.
    </p>

